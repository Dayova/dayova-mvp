import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = process.cwd();
const cssPath = resolve(root, "src/global.css");
const outputPath = resolve(root, "src/lib/theme-variables.ts");
const checkOnly = process.argv.includes("--check");

const css = readFileSync(cssPath, "utf8");
const darkRoot = css.match(/\.dark:root\s*\{([^}]+)\}/)?.[1];
if (!darkRoot) {
	throw new Error("Unable to find the canonical `.dark:root` palette.");
}

const variables = Object.fromEntries(
	[...darkRoot.matchAll(/(--[\w-]+):\s*([^;]+);/g)].map(([, name, value]) => [
		name,
		value.trim(),
	]),
);
if (Object.keys(variables).length === 0) {
	throw new Error("The canonical `.dark:root` palette has no CSS variables.");
}

const generatedEntries = Object.entries(variables)
	.map(
		([name, value]) => `\t${JSON.stringify(name)}: ${JSON.stringify(value)},`,
	)
	.join("\n");

const generated = `/**
 * Generated from \`.dark:root\` in \`src/global.css\` by
 * \`pnpm theme:generate\`. Do not edit this file directly.
 *
 * NativeWind's root-variable observer does not invalidate already-mounted
 * Fabric views after an Appearance override on React Native 0.86, so the app
 * also supplies these generated values through a root runtime variable scope.
 */
export const DARK_THEME_VARIABLES = {
${generatedEntries}
} as const;
`;

if (checkOnly) {
	const current = readFileSync(outputPath, "utf8");
	if (current !== generated) {
		console.error(
			"Native theme variables are stale. Run `pnpm theme:generate` and commit the result.",
		);
		process.exitCode = 1;
	}
} else {
	writeFileSync(outputPath, generated);
}
